const int INF = 0x3f3f3f3f;
const int maxn = 1 << 10;

struct Edge
{
    int from, to, cap, flow;
    Edge(int u, int v, int c, int f) : from(u), to(v), cap(c), flow(f) {}
};

struct Dinic
{
    int n, m, s, t;      //结点数，边数（包括反向弧），源点编号和汇点编号
    vector<Edge> edges;  //边表。edge[e]和edge[e^1]互为反向弧
    vector<int> G[maxn]; //邻接表，G[i][j]表示节点i的第j条边在e数组中的序号
    bool vis[maxn];      //BFS使用
    int d[maxn];         //从起点到i的距离
    int cur[maxn];       //当前弧下标
    void init(int n)
    {
        this->n = n;
        for (int i = 0; i < n; i++) G[i].clear();
        edges.clear();
    }
    void AddEdge(int from, int to, int cap)
    {
        edges.push_back(Edge(from, to, cap, 0));
        edges.push_back(Edge(to, from, 0, 0));
        m = edges.size();
        G[from].push_back(m - 2);
        G[to].push_back(m - 1);
    }
    bool BFS()
    {
        memset(vis, 0, sizeof(vis));
        memset(d, 0, sizeof(d));
        queue<int> q;
        q.push(s);
        d[s] = 0;
        vis[s] = 1;
        while (!q.empty())
        {
            int x = q.front();
            q.pop();
            for (int i = 0; i < G[x].size(); i++)
            {
                Edge& e = edges[G[x][i]];
                if (!vis[e.to] && e.cap > e.flow)
                {
                    vis[e.to] = 1;
                    d[e.to] = d[x] + 1;
                    q.push(e.to);
                }
            }
        }
        return vis[t];
    }
    int DFS(int x, int a)
    {
        if (x == t || a == 0) return a;
        int flow = 0, f;
        for (int& i = cur[x]; i < G[x].size(); i++)
        {
            //从上次考虑的弧
            Edge& e = edges[G[x][i]];
            if (d[x] + 1 == d[e.to] && (f = DFS(e.to, min(a, e.cap - e.flow))) > 0)
            {
                e.flow += f;
                edges[G[x][i] ^ 1].flow -= f;
                flow += f;
                a -= f;
                if (a == 0) break;
            }
        }
        return flow;
    }
    int Maxflow(int s, int t)
    {
        this->s = s;
        this->t = t;
        int flow = 0;
        while (BFS())
        {
            memset(cur, 0, sizeof(cur));
            flow += DFS(s, INF);
        }
        return flow;
    }
    vector<VI> ans;
    void dfs(int u)
    {
        ans.back().push_back(u);
        vis[u] = true;
        for (auto& id : G[u])
        {
            Edge& e = edges[id];
            if (e.to > s && e.to < t && abs(e.flow) == 1 && !vis[e.to])
                dfs(e.to);
        }
    }
    void solve(int n)
    {
        memset(vis, 0, sizeof(vis));
        for (int i = 1; i <= n; i++)
            if (!vis[i])
            {
                ans.push_back(VI());
                dfs(i);
            }
        W(ans.size());
        for (auto& t : ans) W(t.size(), t);
    }

} gao;

bool isprime(int x)
{
    for (int i = 2; i * i <= x; i++)
        if (x % i == 0) return false;
    return x != 1;
}

void go()
{
    int n;
    R(n);
    gao.init(n + 2);
    int s = 0, t = n + 1;
    VI a(n + 1);
    for (int i = 1; i <= n; i++)
    {
        R(a[i]);
        if (a[i] & 1)
            gao.AddEdge(s, i, 2);
        else
            gao.AddEdge(i, t, 2);
    }
    for (int i = 1; i <= n; i++)
        if (a[i] & 1)
            for (int j = 1; j <= n; j++)
                if (!(a[j] & 1))
                    if (isprime(a[i] + a[j]))
                    {
                        debug(i, j);
                        gao.AddEdge(i, j, 1);
                    }
    int flow = gao.Maxflow(s, t);
    debug(flow);
    if (flow != n)
    {
        W("Impossible");
        return;
    }
    gao.solve(n);
}