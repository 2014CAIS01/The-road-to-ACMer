ll dp[22][22][15];
int a[22];

ll dfs(int pos, int cnt = 0, int pre = 0, bool limit = true)
{
    // if(cnt > 3) return 0;
    if (pos == -1) return cnt <= 3;
    if (!limit && ~dp[pos][cnt][pre]) return dp[pos][cnt][pre];
    int up = limit ? a[pos] : 9;
    ll ans = 0;
    for (int i = 0; i <= up; i++)
        ans += dfs(pos - 1, cnt + (i != 0), i, limit && i == a[pos]);
    if (!limit) dp[pos][cnt][pre] = ans;
    return ans;
}

ll solve(ll x)
{
    int pos = 0;
    do
        a[pos++] = x % 10;
    while (x /= 10);
    return dfs(pos - 1);
}

void go()
{
    memset(dp, -1, sizeof(dp));
    int T;
    R(T);
    while (T--)
    {
        ll l, r;
        R(l, r);
        W(solve(r) - solve(l - 1));
    }
}
