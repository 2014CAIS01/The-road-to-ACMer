int main()
{
    int n;
    cin >> n;
    vector<int> a(n);
    for (int i = 0; i < n; i++) cin >> a[i];
    sort(a.begin(), a.end());
    int x = a.back() - a.front() + 1;
    cout << x - n << endl;
    return 0;
}